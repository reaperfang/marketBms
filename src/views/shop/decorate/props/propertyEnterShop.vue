          placeholder="请输入文案"
          v-model="textarea">
        </el-input>
      </el-form-item>
    </div>
  </el-form>
</template>

<script>
export default {
  name: 'propertyEnterShop',
  components: {},
  data () {
    return {
      ruleForm: {
      },
      rules: {

      },

    }
  },
  created() {
    this.initRuleForm();
  },
  watch: {
    ruleForm: {
      handler(newValue) {
        this.emitChangeRuleForm(newValue);
      },
      deep: true
    }
  },
  methods: {

    /* 初始化表单数据 */
    initRuleForm() {
      if(this.data){
        this.ruleForm = this.data;
      }
    },

    /* 发送数据改变事件 */
    emitChangeRuleForm(newValue) {
      this.$emit('change', {
        id: this.$parent.currentComponentId,
        data: newValue
      });
    }
  }
}
</script>

